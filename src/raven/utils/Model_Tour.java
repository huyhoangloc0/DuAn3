
package raven.utils;


public class Model_Tour {
    private String MaTour;
    private String TenTour;
    private String DiaDiem;
    private String SoNgay;
    private String SoDem;
    private String SoNguoi;
    private String MoTa;

    public Model_Tour() {
    }

    public Model_Tour(String MaTour, String TenTour, String DiaDiem, String SoNgay, String SoDem, String SoNguoi, String MoTa) {
        this.MaTour = MaTour;
        this.TenTour = TenTour;
        this.DiaDiem = DiaDiem;
        this.SoNgay = SoNgay;
        this.SoDem = SoDem;
        this.SoNguoi = SoNguoi;
        this.MoTa = MoTa;
    }

    public String getMaTour() {
        return MaTour;
    }

    public void setMaTour(String MaTour) {
        this.MaTour = MaTour;
    }

    public String getTenTour() {
        return TenTour;
    }

    public void setTenTour(String TenTour) {
        this.TenTour = TenTour;
    }

    public String getDiaDiem() {
        return DiaDiem;
    }

    public void setDiaDiem(String DiaDiem) {
        this.DiaDiem = DiaDiem;
    }

    public String getSoNgay() {
        return SoNgay;
    }

    public void setSoNgay(String SoNgay) {
        this.SoNgay = SoNgay;
    }

    public String getSoDem() {
        return SoDem;
    }

    public void setSoDem(String SoDem) {
        this.SoDem = SoDem;
    }

    public String getSoNguoi() {
        return SoNguoi;
    }

    public void setSoNguoi(String SoNguoi) {
        this.SoNguoi = SoNguoi;
    }

    public String getMoTa() {
        return MoTa;
    }

    public void setMoTa(String MoTa) {
        this.MoTa = MoTa;
    }

    
}
